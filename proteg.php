  <h4>  Comment rendre votre environnement plus sûr</h4>
   <ul>
       <li>
           <b>Évitez les espaces clos, très fréquentés, où vous serez en contact étroit avec d'autres personnes.</b>
       </li>
       <li><ul>
       <li>	Des foyers épidémiques ont été signalés dans les restaurants, les chorales, les cours d'entraînement physique, les boîtes de nuit, les bureaux et les lieux de culte où les gens sont rassemblés dans des espaces clos bondés où ils parlent fort, crient, respirent profondément ou chantent.
       </li>
       <li>	Les risques de contracter la COVID-19 sont plus élevés dans les espaces très fréquentés et insuffisamment ventilés où des personnes infectées passent de longs moments ensemble dans une étroite promiscuité. C'est dans ces environnements que le virus semble se propager le plus efficacement par des gouttelettes respiratoires ou des aérosols, d’où l’importance d’y prendre toutes les précautions.
       </li>
       </ul></li>
